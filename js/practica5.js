// Escribe un programa de tres líneas que pida un número, pida otro número y 
// escriba el resultado de sumar estos dos números. 

const primerNumero = parseInt(prompt("Introduce el primer número:"));
const segundoNumero = parseInt(prompt("Introduce el segundo número:"));
document.writeln("La suma de " + primerNumero + " y " + segundoNumero + " es: " + (primerNumero + segundoNumero));
